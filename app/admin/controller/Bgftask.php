    /**
     * 新增任务
     * @return mixed
     * @throws \think\db\exception\DbException
     * @author: fudaoji<fdj@kuryun.cn>
     */
    public function add(){
        $data = [

        ];
        $last_one = $this->model->getOneByOrder([
            'where' => ['admin_id' => $this->adminInfo['id'], 'bot_id' => $this->bot['id']],
            'field' => 'wxids',
            'order' => ['id' => 'desc']
        ]);
        if($last_one){
            $data['wxids'] = explode(',', $last_one['wxids']);
        }

        $groups = model('admin/botMember')->getField('wxid,nickname',['uin' => $this->bot['uin']]);
        $builder = new FormBuilder();
        $builder->setPostUrl(url('savePost'))
            ->addFormItem('goods_id', 'chosen', '选择商品', '选择商品', $this->getGoodsList(), 'required')
            ->addFormItem('wxids', 'chosen_multi', '接收对象', '发送对象', $groups, 'required')
            ->addFormItem('plan_time', 'datetime', '发送时间', '不填则根据设置的发单间隔时间确定', [], '')
            ->addFormItem('remark', 'text', '备注', '')
            ->setFormData($data);
        return $builder->show();
    }

    private function getGoodsList(){
        return $this->agentGoodsM->getIdToTitle();
    }

    /**
     * 编辑任务
     * @return mixed
     * @throws \think\db\exception\DataNotFoundException
     * @throws \think\db\exception\ModelNotFoundException
     * @throws \think\db\exception\DbException
     * @author: fudaoji<fdj@kuryun.cn>
     */
    public function edit(){
        $id = input('id', 0, 'intval');
        if(!$data = $this->model->getOne($id)){
            $this->error('数据不存在');
        }
        $data['wxids'] = explode(',', $data['wxids']);
        $groups = model('admin/botMember')->getField('wxid,nickname',['uin' => $this->bot['uin']]);
        $builder = new FormBuilder();
        $builder->setPostUrl(url('savePost'))
            ->addFormItem('id', 'hidden', 'id', 'id')
            ->addFormItem('goods_id', 'chosen', '选择商品', '选择商品', $this->getGoodsList(), 'required')
            ->addFormItem('wxids', 'chosen_multi', '接收对象', '发送对象', $groups, 'required')
