namespace app\admin\controller;

use app\common\service\DACommunity;

class Notice extends Base
{
    public function initialize()
    {
        parent::initialize(); // TODO: Change the autogenerated stub
    }

    /**
     * 社区公告
     * @return mixed
     * Author: fudaoji<fdj@kuryun.cn>
     */
    public function community(){
        $params = ['current_page' => input('page', 1)];
        if(is_string($res = DACommunity::listNotice($params))){
            $this->error($res);
        }
        $data_list = $res['list'];
        $assign = compact('data_list');
        return $this->show($assign);
    }

    public function hasNewsPost(){
        if(is_string($res = DACommunity::listNotice())){
            $this->error($res);
        }
        $this->success('success', '', $res['list'][0]);
    }
}